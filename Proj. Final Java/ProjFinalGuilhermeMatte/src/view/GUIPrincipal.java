    public void abrirGUIColaboradores() {
        if (!flagGUIColaboradores) {
            //Função para abrir GUIColaboradores
            GUIConsultaColaboradores gcc = new GUIConsultaColaboradores();

            jdpAreaDeTrabalho.add(gcc);
            gcc.setVisible(true);
            flagGUIColaboradores = true;
            gcc.addInternalFrameListener(this);

        }
    }

