    @Override
    public void internalFrameClosed(InternalFrameEvent ife) {
        if (ife.getInternalFrame() instanceof GUICadastro) {
            flagGUICadastro = false;
        }
        if (ife.getInternalFrame() instanceof GUIConsultarComputadores) {
            flagGUIConsultaComputador = false;
        }
        if (ife.getInternalFrame() instanceof GUICadUsuarios) {
            flagGUICadUsuarios = false;
        }
        if (ife.getInternalFrame()instanceof GUIConsultaColaboradores) {
            flagGUIColaboradores = false;
        }
    }

