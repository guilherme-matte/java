    public void logoff() {
        if (!flagGUILogin) {
            //Função para fechar a GUIPrincipal e abrir GUILogin
            GUILogin gl = new GUILogin();

            gl.setVisible(true);

            flagGUILogin = true;

            dispose();

        }
    }

