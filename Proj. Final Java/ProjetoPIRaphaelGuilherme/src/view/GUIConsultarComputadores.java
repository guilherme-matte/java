    public void PreencherTabelaPessoaFisica() {

        try {

            ConPessoaFisicaDAO cDAO = new ConPessoaFisicaDAO();

            ArrayList<ProdutoPessoaFisicaVO> prod = new ArrayList<>();

            prod = cDAO.preencherTabela();

            for (int i = 0; i < prod.size(); i++) {

                dtmPessoaFisica.addRow(new String[]{
                    String.valueOf(prod.get(i).getIdProdutoPF()),
                    String.valueOf(prod.get(i).getCPF()),
                    String.valueOf(prod.get(i).getNomeCompleto()),
                    String.valueOf(prod.get(i).getQuantidade()),
                    String.valueOf(prod.get(i).getCategoria()),
                    String.valueOf(prod.get(i).getDescricao())
                });

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela pessoa física" + e.getMessage());

        }

    }

    public void PreencherTabelaEmpresas() {

        try {

            ConEmpresaDAO cDAO = new ConEmpresaDAO();

            ArrayList<ProdutoEmpresaVO> empresas = new ArrayList<>();

            empresas = cDAO.PreencherTabela();

            for (int i = 0; i < empresas.size(); i++) {
                dtmEmpresa.addRow(new String[]{
                    String.valueOf(empresas.get(i).getIdProduto()),
                    String.valueOf(empresas.get(i).getCNPJ()),
                    String.valueOf(empresas.get(i).getCategoria()),
                    String.valueOf(empresas.get(i).getNomeEmpresa()),
                    String.valueOf(empresas.get(i).getResponsavelEmpresa()),
                    String.valueOf(empresas.get(i).getQuantidade()),
                    String.valueOf(empresas.get(i).getDescricao())
                });
            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela Empresa!" + e.getMessage());

        }
    }

    public void limparTela() {
        if (jtpConsulta.getSelectedIndex() == 0) {

            dtmEmpresa.setNumRows(0);

        } else {

            dtmPessoaFisica.setNumRows(0);

        }
    }

