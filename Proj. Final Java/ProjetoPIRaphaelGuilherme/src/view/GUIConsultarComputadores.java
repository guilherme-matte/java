    public void pesquisarProdEmpresa() {
        try {

            if (jtfConsultaEmpresa.getText().isEmpty()) {

                limparTela();

                PreencherTabelaEmpresas();

            } else {

                String opcao = null, pesquisa = null;

                switch (jcbConsultaEmpresa.getSelectedItem().toString()) {

                    case "ID Produto":
                        opcao = "produto_id";
                        break;

                    case "CNPJ":
                        opcao = "cnpj";
                        break;

                    case "Categoria":
                        opcao = "categoria";
                        break;

                    case "Nome da Empresa":
                        opcao = "nomeEmpresa";
                        break;

                    case "Respnsável":
                        opcao = "responsavel";
                        break;

                    case "Quantidade":
                        opcao = "quantidade";
                        break;

                    case "Descrição":
                        opcao = "descricao";
                }//FIM SWITCH
                pesquisa = jtfConsultaEmpresa.getText();

                ConEmpresaDAO cDAO = new ConEmpresaDAO();

                ArrayList<ProdutoEmpresaVO> prod = new ArrayList<>();

                prod = cDAO.FiltrarProdutoEmpresa(opcao, pesquisa);

                
                for (int i = 0; i < prod.size(); i++) {
                    dtmEmpresa.addRow(new String[]{
                        String.valueOf(prod.get(i).getIdProduto()),
                        String.valueOf(prod.get(i).getCNPJ()),
                        String.valueOf(prod.get(i).getCategoria()),
                        String.valueOf(prod.get(i).getNomeEmpresa()),
                        String.valueOf(prod.get(i).getResponsavelEmpresa()),
                        String.valueOf(prod.get(i).getQuantidade()),
                        String.valueOf(prod.get(i).getDescricao())
                    });
                }
            }
            jtbConsultaEmpresa.setModel(dtmEmpresa);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro! GUIConsultaComputadores.PesquisarProdEmpresa" + e.getMessage());
        }
    }

